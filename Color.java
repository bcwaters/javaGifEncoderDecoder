public class Color {

	private byte red;

	private byte green;

	private byte blue;

	public Color(byte _red, byte _green, byte _blue) {
		red = _red;
		green = _green;
		blue = _blue;
	}

	public byte getRed() {
		return red;
	}

	public byte getBlue() {
		return blue;
	}

	public byte getGreen() {
		return green;
	}

}
